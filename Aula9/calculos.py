def soma(num1,num2):
    soma=num1+num2
    return soma
    

def sub(num1,num2):
    sub=num1-num2
    return sub    
   

def mult(num1,num2):
    mult=num1*num2
    return mult

def div_inteira(num1,num2):
    div_inteira=num1/num2
    return div_inteira

def div_fra(num1,num2):
    div_fra=num1//num2
    return div_fra

def resto(num1,num2):
    resto=num1%num2
    return resto

def raiz(num1,num2):    
    raiz=num1**(1/num2)
    return raiz