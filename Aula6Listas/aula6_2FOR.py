
#Estruturas de Repetição - FOR

# For Simpres, usando range com incremento padrão de 1

for i in range(0,10): #termina antes do último número--- nesse caso no número 9
    print(f'{i} - Eu te amo')
print(' \n\n')
#For Simples com range, incremento de 2

for i in range(0,100,2):
    print(f'{i} - Pares')    

